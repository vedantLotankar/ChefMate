// Helper function to get recipe by ID
export const getRecipeById = (id: string): Recipe | undefined => {
  return dummyRecipes.find(recipe => recipe.id === id);
};

// Helper function to get recipes by category
export const getRecipesByCategory = (category: string): Recipe[] => {
  return dummyRecipes.filter(recipe => recipe.category === category);
};

// Helper function to search recipes
export const searchRecipes = (query: string): Recipe[] => {
  const lowercaseQuery = query.toLowerCase();
  return dummyRecipes.filter(recipe => 
    recipe.name.toLowerCase().includes(lowercaseQuery) ||
    recipe.description?.toLowerCase().includes(lowercaseQuery) ||
    recipe.tags?.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};
